public class lesson6 {
    public static void main(String[] args) {
        // task 1
        /*
        Продолжите работать с кодом, который вы написали в предыдущем уроке — в задачах с накоплениями.
        С помощью цикла while посчитайте, сколько месяцев потребуется, чтобы накопить 2 459 000 рублей при условии,
        что первоначально мы имеем 0 рублей и готовы откладывать по 15 тысяч рублей.
        Результат программы должен быть выведен в консоль с тем количеством месяцев,
        которое необходимо для накопления данной суммы. Формат сообщения: «Месяц …,
        сумма накоплений равна … рублей».
         */
        int savings = 15000;
        int total = 0;
        int month = 0;
        int finalTotal = 2_459_000;

        while (total <= finalTotal) {
            month++;
            total = total + savings;
        }
        System.out.println("Месяц " + month + ", сумма накоплений равна " + finalTotal + " рублей");

        // task 2
        /*
        Выведите в одну строку через пробел числа от 1 до 10 с помощью цикла while.
        На следующей строке выведите числа в обратном порядке от 10 до 1 с помощью цикла for.
        Для обоих циклов можно использовать как одну общую переменную, так и свою в каждом цикле.
         */
        int a = 0;
        byte b = 11;
        while (a < 10) {
            a = a + 1;
            System.out.print(a + " ");
            if (a == 10) {
                System.out.println();
            }
        }
        while (b > 1) {
            b--;
            System.out.print(b + " ");
            if (b == 1) {
                System.out.println();
            }
        }

        // task 3
        /*
        В стране Y население — 12 миллионов человек.
        Рождаемость составляет 17 человек на 1000, смертность — 8 человек. Рассчитайте,
        какая численность населения будет через 10 лет, если показатели рождаемости и смертности постоянны.
        В консоль выведите результат операции на каждый год в формате: «Год …, численность населения составляет …».
         */
        // Исходные данные
        double population = 12000000;
        double birthRate = 17.0 / 1000;
        double deathRate = 8.0 / 1000;
        int years = 10;

        for (int year = 1; year <= years; year++) {
            // Вычисляем количество родившихся и умерших
            double births = population * birthRate;
            double deaths = population * deathRate;

            // Обновляем численность населения
            population = population + births - deaths;

            // Выводим результат для текущего года
            System.out.println("Год " + year + " численность населения составляет " + (int) population);
        }

         // task 4
        /*
        Василий решил положить деньги на накопительный счет, где каждый месяц к сумме его вклада добавляется еще 7%.
        Первоначальная сумма вклада — 15 тысяч рублей.
        Вычислите и выведите в консоль, сколько месяцев Василию нужно будет копить,
        чтобы собрать сумму в 12 миллионов рублей при условии, что процент банка от накоплений не меняется,
        а всегда равен 7%.
        Выведите в консоль результат программы с указанием суммы накоплений по каждому месяцу.
         */
        double startCapital = 15000;
        int totalCapital = 12_000_000;
        double rate = 0.07;
        int monthBank = 0;
        while (startCapital <= totalCapital) {
            startCapital = startCapital + startCapital * rate;
            monthBank++;
            System.out.println("Время затрачено в месяцах : " + monthBank + ". Накопленный капитал: " + (int) startCapital);
        }

        // task 5
        /*
        Видоизмените программу таким образом, чтобы в консоль выводились не все месяцы подряд, а только каждый шестой.
        Должны быть видны накопления за 6-й, 12-й, 18-й, 24-й и следующие месяцы.
         */
        double startCapital1 = 15000;
        int totalCapital1 = 12_000_000;
        double rate1 = 0.07;
        int monthBank1 = 0;
        while (startCapital1 <= totalCapital1) {
            startCapital1 = startCapital1 + startCapital1 * rate1;
            monthBank1++;
            if (monthBank1 % 6 == 0) {
                System.out.println("Время затрачено в месяцах : " + monthBank1 + ". Накопленный капитал: " + (int) startCapital1);
            }
        }
        System.out.println("Время затрачено в месяцах : " + monthBank1 + ". Накопленный капитал: " + (int) startCapital1);

        // task 6
        /*
        Василий решил, что будет копить деньги ближайшие 9 лет.
        Он хочет знать, какой будет сумма его накоплений каждые полгода на протяжении этих 9 лет.
        Исходная сумма всё та же — 15 тысяч рублей, проценты банка — 7% ежемесячно.
        Напишите программу, которая будет выводить сумму накоплений за каждые полгода в течение 9 лет.
         */
        double startCapitalV = 15000;
        double rateB = 0.07;
        int monthBankV0 = 0;
        int monthBankV = 9 * 12;
        while (monthBankV0 <= monthBankV) {
            startCapitalV = startCapitalV + startCapitalV * rateB;
            monthBankV0++;
            if (monthBankV0 % 6 == 0) {
                System.out.println("Время затрачено в месяцах : " + monthBankV0 + ". Накопленный капитал: " + (int) startCapitalV);
            }
        }
        // переведем месяца в года для вывода в консоль количества лет
        int yearBankV = monthBankV0 / 12;
        System.out.println("Время затрачено в годах : " + yearBankV + ". Накопленный капитал: " + (int) startCapitalV);

        // task 7
        /*
        В компании пятница — отчетный день.
        Нужно написать программу, которая считает дни месяца по датам, определяет,
        какой день — пятница, и выводит сообщение с напоминанием, что нужно подготовить еженедельный отчет.
        Создайте переменную типа int, которая хранит в себе номер первой пятницы месяца (число от 1 до 7).
        Выведите на каждую пятницу месяца (включая полученную) сообщение следующего вида: «Сегодня пятница, ...-е число. Необходимо подготовить отчет».
        В нашем месяце 31 день. В результате у вас должно получиться от 4 до 5 сообщений с напоминаниями по разным датам.
         */
        int firstFriday = 3;
        for (; firstFriday <= 31; firstFriday = firstFriday + 7) {
            System.out.println("Сегодня пятница, " + firstFriday + " число. Необходимо подготовить отчет");
        }

        // task 8
        /*
        Нам нужно написать астрономическое приложение, которое считает, когда над Землей пролетает комета.
        Известно, что комета пролетает каждый 79-й год, начиная с нулевого.
        В консоль нужно вывести все годы за последние 200 лет, когда появлялась комета,
        а также следующий год ее появления (ближайшие 100 лет).
        Для вычисления периода можно создать две дополнительные переменные,
        которые содержат год за 200 лет до текущего (из созданной ранее переменной) в качестве старта и
        100 лет после в качестве завершения периода расчета.
         */
        int startCentury = 0;
        for (; startCentury < 2124; startCentury = startCentury + 79) {
            if (startCentury >= 1824) {
                while (startCentury < 2124) {
                    System.out.println(startCentury);
                    break;
                }
            }
        }
    }
}

